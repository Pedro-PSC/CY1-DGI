def Soma(a,b):
    """
    Esta função soma o valor de 'a' e 'b'
    """
    return a+b

def Subtracao(a,b):
    """
    Esta função subtrai o valor de 'a' e 'b'
    """
    return a-b

def Multiplicacao(a,b):
    """
    Esta função multiplica o valor de 'a' e 'b'
    """
    return a*b

def Divisao(a,b):
    """
    Esta função divisão o valor de 'a' e 'b'
    """
    return a/b